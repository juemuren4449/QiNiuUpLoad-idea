package com.juemuren;

/**
 * 返回结果实体类
 * 
 * @author juemuren
 *
 */
public class Result {

	/**
	 * 返回结果事例
	 * hash : Fh8xVqod2MQ1mocfI4S4KpRL6D98 
	 * key : gogopher.jpg
	 */

	private String hash;
	private String key;

	public String getHash() {
		return hash;
	}

	public void setHash(String hash) {
		this.hash = hash;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
}
